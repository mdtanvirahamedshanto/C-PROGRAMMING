// Output Variable Size 

#include<stdio.h>
int main()
{
    int i;
    float k;
    double z;
    char j;

    printf("Size Of int = %d\n",sizeof(i));
    printf("Size Of float = %d\n",sizeof(k));
    printf("Size Of double = %d\n",sizeof(z));
    printf("Size Of Char = %d \n",sizeof(j));

return 0;

}